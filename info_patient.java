    private void jButtonOKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonOKActionPerformed
        PreparedStatement pt;
        try{
            Class.forName("com.mysql.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost/tablas","root", "12345");
            pt = con.prepareStatement("INSERT INTO patient(patient_id,name_patient,last_name,age,curp,nss,sexo,weight,height,blood,allergies,past) VALUES (?,?,?,?,?,?,?,?,?,?,?,?)");
            pt.setString(1, jTFID.getText());
            pt.setString(2, jTFnombre.getText());
            pt.setString(3, jTFapellido.getText());
            pt.setString(4, jTFfechanacimiento.getText());
            pt.setString(5, jTFcurp.getText());
            pt.setString(6, jTFnss.getText());
            pt.setString(7, jTFsexo.getText());
            pt.setString(8, jTFpeso.getText());
            pt.setString(9, jTFaltura.getText());
            pt.setString(10, jTFtiposangre.getText());
            pt.setString(11, jTFalergias.getText());
            pt.setString(12, jTFantecedentes.getText());
            pt.executeUpdate();
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e.getMessage() ,"Error", 1);
        }
        nuevo.setVisible(true);
        this.setVisible(false);
    }//GEN-LAST:event_jButtonOKActionPerformed

